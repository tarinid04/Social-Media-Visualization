package prj5;

/**
 * Enumeration representing months with additional DEFAULT value. This enum
 * provides a set of predefined constants for each month and a DEFAULT value.
 * The DEFAULT value is used when a month is not explicitly specified or
 * recognized.
 *
 * @author Tarini Duvvuri (tarinid)
 * @version 18 Nov 2023
 */
public enum MonthEnum
{
    JANUARY,
    FEBRUARY,
    MARCH,
    APRIL,
    MAY,
    JUNE,
    JULY,
    AUGUST,
    SEPTEMBER,
    OCTOBER,
    NOVEMBER,
    DECEMBER,
    DEFAULT
}
